// Contoh struktur data array

let buah = ["Apel", "Jeruk", "Mangga"];

// Mengakses elemen array
console.log(buah[0]); // Apel

// Menambah elemen ke array
buah.push("Durian");
console.log(buah);

// Menghapus elemen dari array
buah.pop();
console.log(buah);

// Looping array
for (let i = 0; i < buah.length; i++) {
    console.log(buah[i]);
}