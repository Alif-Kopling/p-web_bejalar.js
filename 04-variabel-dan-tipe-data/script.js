// Contoh variabel dan tipe data

// String
let nama = "Budi";
console.log("Nama: " + nama);

// Number
let umur = 25;
console.log("Umur: " + umur);

// Boolean
let sudahMenikah = false;
console.log("Sudah Menikah: " + sudahMenikah);

// Array
let hobi = ["membaca", "menulis", "bermain game"];
console.log("Hobi: " + hobi[0]);

// Object
let alamat = {
    jalan: "Jl. Pahlawan No. 10",
    kota: "Bandung"
};
console.log("Alamat: " + alamat.jalan + ", " + alamat.kota);