// Contoh operator

// Operator Aritmatika
let a = 10;
let b = 5;
console.log("a + b = " + (a + b));
console.log("a - b = " + (a - b));
console.log("a * b = " + (a * b));
console.log("a / b = " + (a / b));
console.log("a % b = " + (a % b));

// Operator Penugasan
let c = 10;
c += 5; // c = c + 5
console.log("c = " + c);

// Operator Perbandingan
let d = 10;
let e = "10";
console.log("d == e: " + (d == e)); // true
console.log("d === e: " + (d === e)); // false

// Operator Logika
let f = true;
let g = false;
console.log("f && g: " + (f && g)); // false
console.log("f || g: " + (f || g)); // true
console.log("!f: " + !f); // false