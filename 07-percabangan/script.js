// Contoh percabangan

// if-else
let nilai = 75;
if (nilai >= 70) {
    console.log("Anda lulus!");
} else {
    console.log("Anda tidak lulus.");
}

// switch-case
let hari = "Senin";
switch (hari) {
    case "Senin":
        console.log("Hari ini hari Senin.");
        break;
    case "Selasa":
        console.log("Hari ini hari Selasa.");
        break;
    default:
        console.log("Hari ini bukan hari Senin atau Selasa.");
}